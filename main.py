    if packet.haslayer(DNS) and packet.haslayer(DNSQR):
        query_name = packet[DNSQR].qname.decode(errors="ignore")
        if re.search(airdrop_pattern, query_name):
            print(f"\nğŸ“¡ AirDrop Discovered!")
